public class Ex03 {
    
    public static void main(String[] args) {
        double x=3.0,y=4.0,z=-5.0;
        
        System.out.println("==X==");
        System.out.println("Raiz quadrada: "+Math.sqrt(x));
        System.out.println("X elevado a 4: "+Math.pow(x,4));
        System.out.println("Valor absoluto: "+Math.abs(x));
        
        System.out.println("\n==Y==");
        System.out.println("Raiz quadrada: "+Math.sqrt(y));
        System.out.println("Y elevado a 4: "+Math.pow(y,4));
        System.out.println("Valor absoluto: "+Math.abs(y));

        System.out.println("\n==Z==");
        System.out.println("Raiz quadrada: "+Math.sqrt(z));
        System.out.println("Z elevado a 4: "+Math.pow(z,4));
        System.out.println("Valor absoluto: "+Math.abs(z));        
    }
    
}
