public class Professor {

  
    private String nome;
    private int idade;
    private String dpto;
    private double salario;

    public Professor(String nome, int idade, String dpto, double salario) {
        this.nome = nome;
        this.idade = idade;
        this.dpto = dpto;
        this.salario = salario;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getDpto() {
        return dpto;
    }

    public void setDpto(String dpto) {
        this.dpto = dpto;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }
    
    
    
    
    
    
    
    
}
